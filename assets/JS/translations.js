// Function to detect user's preferred language
function detectUserLanguage() {
  const userLang = navigator.language || navigator.userLanguage;
  const portugueseSpeakingCountries = ['pt', 'pt-br', 'pt-pt', 'pt-mz', 'pt-ao', 'pt-cv', 'pt-gw', 'pt-st', 'pt-tl'];
  return portugueseSpeakingCountries.includes(userLang.toLowerCase()) ? 'pt' : 'en';
}

// Function to update text content based on language
function updateContent(lang) {
  try {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
      const key = element.getAttribute('data-translate');
      if (translations[lang] && translations[lang][key]) {
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
          element.placeholder = translations[lang][key];
        } else {
          element.textContent = translations[lang][key];
        }
      }
    });
    
    // Update language switcher button text
    const languageBtn = document.getElementById('languageBtn');
    if (languageBtn) {
      languageBtn.textContent = lang === 'en' ? translations.pt.switchToPortuguese : translations.en.switchToEnglish;
    }
  } catch (error) {
    console.error('Error updating content:', error);
  }
}

// Function to toggle language
function toggleLanguage() {
  try {
    const currentLang = document.documentElement.lang || 'en';
    const newLang = currentLang === 'en' ? 'pt' : 'en';
    document.documentElement.lang = newLang;
    updateContent(newLang);
  } catch (error) {
    console.error('Error toggling language:', error);
  }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  try {
    const initialLang = detectUserLanguage();
    document.documentElement.lang = initialLang;
    updateContent(initialLang);
  } catch (error) {
    console.error('Error initializing language:', error);
  }
}); 